  WRITE (ILUN) KTOPC_1(:,JBLK1)         
  WRITE (ILUN) KBASEC_1(:,JBLK1)        
  WRITE (ILUN) KTYPE_1(:,JBLK1)         
  WRITE (ILUN) KCBOT_1(:,JBLK1)         
  WRITE (ILUN) KCTOP_1(:,JBLK1)         
  WRITE (ILUN) KBOTSC_1(:,JBLK1)        
