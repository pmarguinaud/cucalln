  WRITE (ILUN) PFHPCL_1(:,:,JBLK1)      
  WRITE (ILUN) PFHPCN_1(:,:,JBLK1)      
  WRITE (ILUN) PFPLCL_1(:,:,JBLK1)      
  WRITE (ILUN) PFPLCN_1(:,:,JBLK1)      
  WRITE (ILUN) PLRAIN_1(:,:,JBLK1)      
  WRITE (ILUN) PRSUD_1(:,:,:,JBLK1)     
