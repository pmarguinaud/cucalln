DO JBLK = 1, NGPBLKS

!$acc loop vector private (KIDIA, KFDIA, YLSTACK)
  DO JLON = 1, NPROMA
    
    YLSTACK%L = LOC (ZSTACK (1, JBLK))
    YLSTACK%U = YLSTACK%L + SIZE (ZSTACK, 1) * KIND (ZSTACK)

