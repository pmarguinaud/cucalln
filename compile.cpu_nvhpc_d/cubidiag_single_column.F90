SUBROUTINE CUBIDIAG_SINGLE_COLUMN (KIDIA, KFDIA, KLON,&
& KLEV, KCTOP, LD_LCUMASK, PA, PB, PR, PU, YDSTACK)
!$acc routine (CUBIDIAG_SINGLE_COLUMN) seq
USE PARKIND1,ONLY:JPIM, JPRB

USE STACK_MOD
#include "stack.h"

IMPLICIT NONE

INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
INTEGER (KIND=JPIM), INTENT (IN)::KCTOP (KLON)
LOGICAL, INTENT (IN)::LD_LCUMASK (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PA (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PB (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PR (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PU (KLON, KLEV)
TYPE(STACK) :: YDSTACK
TYPE(STACK) :: YLSTACK

INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::JK
REAL (KIND=JPRB)::ZBET


YLSTACK = YDSTACK




JLON = KIDIA

PU (JLON,:)=0.0_JPRB

DO JK=2, KLEV

  

  IF (LD_LCUMASK (JLON, JK)) THEN

    IF (JK==KCTOP (JLON)-1) THEN
      ZBET=1.0_JPRB/(PB (JLON, JK)+1.E-35_JPRB)
      PU (JLON, JK)=PR (JLON, JK)*ZBET
    ELSEIF (JK>KCTOP (JLON)-1) THEN
      ZBET=1.0_JPRB/(PB (JLON, JK)+1.E-35_JPRB)
      PU (JLON, JK)=(PR (JLON, JK)-PA (JLON, JK)*PU (JLON, JK-1))*ZBET
    ENDIF

  ENDIF

  
ENDDO


ENDSUBROUTINE CUBIDIAG_SINGLE_COLUMN

