  ITOPM2=KCTOP (JLON)
  ZPBMPT=PAPH (JLON, IKB)-PAPH (JLON, ITOPM2)

  IF (KTYPE (JLON)==1.AND.ZPBMPT<YDML_PHY_EC%YRECUMF%RDEPTHS)  THEN
      KTYPE (JLON)=2
  ENDIF


  IF (KTYPE (JLON)==2.AND.ZPBMPT>=YDML_PHY_EC%YRECUMF%RDEPTHS)  THEN
      KTYPE (JLON)=1
  ENDIF

  ICTOP0 (JLON)=KCTOP (JLON)
