      &*(PQEN (JLON, JK-1)-PQEN (JLON, JK)))*(YDCST%RG*(PMFU (JLON, JK)+PMFD (JLON, JK)))
    ELSE
      ZHEAT =ZHEAT +MAX (0.0_JPRB,((PTEN (JLON, JK-1)-PTEN (JLON, JK)+ZDZ*ZORCPD)/ZTENH (JLON, JK)+YDCST&
