ZHEAT =0.0_JPRB
ZCAPE =0.0_JPRB
ZMFUB1 =ZMFUB (JLON)


DO JK=1, KLEV
  
  LLO1=LDCUM (JLON).AND.KTYPE (JLON)==1

  IF (LLO1.AND.JK<=KCBOT (JLON).AND.JK>KCTOP (JLON)) THEN
    ZDZ=(PGEOH (JLON, JK-1)-PGEOH (JLON, JK))

    IF (LDTDKMF) THEN
