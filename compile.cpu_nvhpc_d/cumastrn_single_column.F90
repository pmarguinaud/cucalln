&(JLON, KLEV)*PQHFL (JLON, KLEV+1))/(PPLRG*PPLDARE)
ZKHFL =(-PAHFS (JLON, KLEV+1)-YDCST%RLVTT*PQHFL (JLON, KLEV+1))/(PPLRG*PPLDARE)


