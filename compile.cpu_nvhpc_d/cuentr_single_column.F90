SUBROUTINE CUENTR_SINGLE_COLUMN (YDCST, YDECUMF, KIDIA, KFDIA, KLON, KLEV, KSPPN2D, KK, KCBOT&
&, KTYPE, LDCUM, LDWORK, PQSEN, PAPH, PGEOH, PMFU, PGP2DSPP, PDMFEN, PDMFDE, YDSTACK)
!$acc routine (CUENTR_SINGLE_COLUMN) seq
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMCST,ONLY:TCST
USE YOECUMF,ONLY:TECUMF
USE SPP_MOD,ONLY:YSPP_CONFIG, YSPP
USE STACK_MOD
#include "stack.h"

IMPLICIT NONE

TYPE (TCST), INTENT (IN)::YDCST
TYPE (TECUMF), INTENT (IN)::YDECUMF
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
INTEGER (KIND=JPIM), INTENT (IN)::KSPPN2D
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KK
INTEGER (KIND=JPIM), INTENT (IN)::KCBOT (KLON)
INTEGER (KIND=JPIM), INTENT (IN)::KTYPE (KLON)
LOGICAL, INTENT (IN)::LDCUM (KLON)
LOGICAL, INTENT (IN)::LDWORK
REAL (KIND=JPRB), INTENT (IN)::PQSEN (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPH (KLON, KLEV+1)
REAL (KIND=JPRB), INTENT (IN)::PGEOH (KLON, KLEV+1)
REAL (KIND=JPRB), INTENT (IN)::PMFU (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PGP2DSPP (KLON, KSPPN2D)
REAL (KIND=JPRB), INTENT (OUT)::PDMFEN (KLON)
REAL (KIND=JPRB), INTENT (OUT)::PDMFDE (KLON)
TYPE(STACK) :: YDSTACK
TYPE(STACK) :: YLSTACK
LOGICAL::LLO1
INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::IPDETRPEN

REAL (KIND=JPRB)::ZXDETRPEN
REAL (KIND=JPRB)::ZMU
REAL (KIND=JPRB)::ZRG
REAL (KIND=JPRB)::ZMF
REAL (KIND=JPRB)::ZENTR 
REAL (KIND=JPRB)::ZDZ


YLSTACK = YDSTACK




JLON = KIDIA



IF (LDWORK) THEN
  ZRG=1.0_JPRB/YDCST%RG
  
  
  PDMFEN (JLON)=0.0_JPRB
  PDMFDE (JLON)=0.0_JPRB
  ZENTR =0.0

  

  

  IF (LDCUM (JLON)) THEN
    ZDZ=(PGEOH (JLON, KK)-PGEOH (JLON, KK+1))*ZRG
    ZMF=PMFU (JLON, KK+1)*ZDZ
    LLO1=KK<KCBOT (JLON)

    IF (LLO1) THEN
      
      ZXDETRPEN=YDECUMF%DETRPEN
      
      PDMFEN (JLON)=ZENTR *ZMF
      PDMFDE (JLON)=ZXDETRPEN*ZMF
    ENDIF

  ENDIF

  
ENDIF



ENDSUBROUTINE CUENTR_SINGLE_COLUMN

